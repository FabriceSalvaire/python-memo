#r# The syntax of a If statement is:

#l# if something_is_true:
#l#     # do something
#l#     pass
#l# else:
#l#     # do something else
#l#     pass

#r# Conditional expressions provides a shorter syntax:

#l# result = 'foo' if something_is_true else 'bar'
